#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*tmp;

	tmp = (t_list *)malloc(sizeof(t_list));
	if (tmp == ((void *)0))
		return ((void *)0);
	tmp->content = content;
	tmp->next = ((void *)0);
	return (tmp);
}
